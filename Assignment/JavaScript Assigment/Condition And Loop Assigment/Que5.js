//  Que -5 - Use the While loop to print number from 1 to 25 in ascending and descending Order

console.log("Print in Ascending Order using While Loop")
let i=1
while(i<=25){
    console.log(i)
    i++
}

console.log("Print in Descending Order using While Loop")
j=25
while(j>=1){
    console.log(j)
    j--
}